package nl.hva.dp2.devices;

public class CeilingLight
{
	public void on()
	{
		System.out.println("Ceiling light turned on");
	}
	
	public void off()
	{
		System.out.println("Ceiling light turned off");
	}
	
	public void dimUp()
	{
		System.out.println("Ceiling light dimmed up");
	}
	
	public void dimDown()
	{
		System.out.println("Ceiling light dimmed down");
	}
}
