package nl.hva.dp2.devices;

public class GarageDoor
{
	public void up()
	{
		System.out.println("Garage door is up");
	}
	
	public void down()
	{
		System.out.println("Garage door is down");
	}
	
	public void stop()
	{
		System.out.println("Garage door stopped");
	}
	
	public void lightOn()
	{
		System.out.println("Garage door lights are on");
	}
	
	public void lightOff()
	{
		System.out.println("Garage door lights are off");
	}
}
